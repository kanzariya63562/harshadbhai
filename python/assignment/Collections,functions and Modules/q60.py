# Write a Python program to calculate the area of a trapezoid.




a = float(input("Enter the upper side of Trapezium : "))
b = float(input("Enter the lower side of Trapezium : "))
h = float(input("Enter the height side of Trapezium : "))



Area=(a+b)*h*0.5


print("The area of the Trapezium is ", Area)