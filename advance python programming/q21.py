# What are oops concepts? Is multiple inheritance supported in java.




# The main ideas behind Java's Object-Oriented Programming, OOP concepts include abstraction, encapsulation, inheritance and polymorphism. Basically, Java OOP concepts let us create working methods and variables, then re-use all or part of them without compromising security.




# Java disallows multiple inheritance to avoid the complexity and ambiguity associated with it, particularly the "diamond problem," where a class inherits from two classes that have a common ancestor, leading to conflicts in the inheritance of methods.